#include <unistd.h>

void ft_print_comb2(void){
	char f1, f2, s1, s2;

	f1='0'; f2='0';
	while(1){
		if(f2 == '9'){
			s1 = f1+1;
			s2 = '0';
		}
		else {
			s1 = f1; 
			s2 = f2+1;
		}
		while(1){
			write(1, &f1, 1);
			write(1, &f2, 1);
			write(1, " ", 1);
			write(1, &s1, 1);
			write(1, &s2, 1);
			if(f1 == '9' && f2 == '8' && s1 == '9' && s2 == '9'){}
			else write(1, ", ", 2);

			if(s2 == '9'){
				if(s1 == '9') break;
				s1++;
				s2 = '0';
			}
			s2++;
		}
		if(f1 == '9' && f2 == '8') break;
		if(f2 == '9'){
			f1++;
			f2 = '0';
		}
		else f2++;
	}
}
